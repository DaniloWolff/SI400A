    /*Inicializar combo usando modelo*/
    private void inicializarComboBoxDatas(){
        List<String> nomeBancos = anoBancos.datasAno;
        DefaultComboBoxModel model = new DefaultComboBoxModel(nomeBancos.toArray(new String[nomeBancos.size()]));
        this.cmbDatas.setModel(model);
    }
    
    /*Inicializar combo usando metodo addItem do combobox para incluir a opcao TODAS as datas*/
    private void inicializarComboBoxDatasTodas(){
        List<String> anoDatas = anoBancos.datasAno;

        this.cmbDatas.addItem("TODAS");
        for(String nome: anoDatas){
                    this.cmbDatas.addItem(nome);
        }
    }
    
    
    
