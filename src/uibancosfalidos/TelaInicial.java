     private void initFX(JFXPanel panel, Banco aluno) {
        final CategoryAxis xAxis = new CategoryAxis();
        final NumberAxis yAxis = new NumberAxis();
         xAxis.setLabel("Per√≠odo");
         yAxis.setLabel("Ano");

        final BarChart<String,Number> bc = 
            new BarChart<>(xAxis,yAxis);
       
        bc.setTitle("Informa√ß√µes do banco : " + aluno.getNomebanco());
               
        XYChart.Series series1 = new XYChart.Series();
        series1.setName("Ano que Faliu");
        
        for(String nomeDisciplina: this.anoBancos.datasAno){
            series1.getData().add(new XYChart.Data(nomeDisciplina, aluno.retornarNota(nomeDisciplina)));
        }
        
        bc.getData().addAll(series1);
        Scene  scene  =  new  Scene(bc, 800,600);
        panel.setScene(scene);
    }
    
